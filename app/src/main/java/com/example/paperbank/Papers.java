package com.example.paperbank;

public class Papers {

    String paper_id;
    String paper_path;
    String paper_year;
    String paper_size;
    String Paper_name;

    public String getPaper_id() {
        return paper_id;
    }

    public void setPaper_id(String paper_id) {
        this.paper_id = paper_id;
    }

    public String getPaper_path() {
        return paper_path;
    }

    public void setPaper_path(String paper_path) {
        this.paper_path = paper_path;
    }

    public String getPaper_year() {
        return paper_year;
    }

    public void setPaper_year(String paper_year) {
        this.paper_year = paper_year;
    }

    public String getPaper_size() {
        return paper_size;
    }

    public void setPaper_size(String paper_size) {
        this.paper_size = paper_size;
    }

    public String getPaper_name() {
        return Paper_name;
    }

    public void setPaper_name(String paper_name) {
        Paper_name = paper_name;
    }
}
